// Check whether the time is morning or not using if...else statement

#include <stdio.h>
int main()
{
    int time=10;
    if(time == 10){
        printf("Good morning.\n");
        printf("Have a nice day.");
    }else{
        printf("Sorry, Its not morning.\n");
        printf("Have a nice day.");
    }

    return 0;
}